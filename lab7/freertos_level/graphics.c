void MKII_Init(void){
	BSP_Button1_Init();
  BSP_Button2_Init();
  BSP_Joystick_Init();

  BSP_RGB_Init(0, 0, 0);
  BSP_Buzzer_Init(0);
	
	BSP_Accelerometer_Init();

	BSP_Buzzer_Set(40);
	BSP_LCD_Init();
  BSP_LCD_FillScreen(BSP_LCD_Color565(0, 0, 0));  // black
	BSP_Buzzer_Set(0);
}

void Report_Accelerometers(void)
{
#define BUTTON1   (*((volatile uint32_t *)0x40007100))  /* PD6 */
#define BUTTON2   (*((volatile uint32_t *)0x40007200))  /* PD7 */
	
uint16_t AccX, AccY, AccZ;
			BSP_Accelerometer_Input(&AccX, &AccY, &AccZ);
			BSP_LCD_DrawString(0, 5, "AccX=    ", BSP_LCD_Color565(255, 255, 255));
      BSP_LCD_SetCursor(5, 5);
      BSP_LCD_OutUDec((uint32_t)AccX, BSP_LCD_Color565(255, 0, 255));
      BSP_LCD_DrawString(0, 6, "AccY=    ", BSP_LCD_Color565(255, 255, 255));
      BSP_LCD_SetCursor(5, 6);
      BSP_LCD_OutUDec((uint32_t)AccY, BSP_LCD_Color565(255, 0, 255));
      BSP_LCD_DrawString(0, 7, "AccZ=    ", BSP_LCD_Color565(255, 255, 255));
      BSP_LCD_SetCursor(5, 7);
      BSP_LCD_OutUDec((uint32_t)AccZ, BSP_LCD_Color565(255, 0, 255));
			BSP_LCD_DrawString(0, 8, "But1=    ", BSP_LCD_Color565(255, 255, 255));
      BSP_LCD_SetCursor(5, 8);
      BSP_LCD_OutUDec((uint32_t)BUTTON1, BSP_LCD_Color565(255, 0, 255));
			BSP_LCD_DrawString(0, 9, "But1=    ", BSP_LCD_Color565(255, 255, 255));
      BSP_LCD_SetCursor(5, 9);
      BSP_LCD_OutUDec((uint32_t)BUTTON2, BSP_LCD_Color565(255, 0, 255));
}

